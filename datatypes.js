// String değişken tanımlama ve typeof ile veri türünü kontrol etme
var stringVariable = "Bu bir string değişken";
console.log("String Değişken Türü:", typeof stringVariable);

// Number değişken tanımlama ve typeof ile veri türünü kontrol etme
var numberVariable = 42;
console.log("Number Değişken Türü:", typeof numberVariable);

// Boolean değişken tanımlama ve typeof ile veri türünü kontrol etme
var booleanVariable = true;
console.log("Boolean Değişken Türü:", typeof booleanVariable);

// Object değişken tanımlama ve typeof ile veri türünü kontrol etme
var objectVariable = { key: "value" };
console.log("Object Değişken Türü:", typeof objectVariable);

// Array değişken tanımlama ve typeof ile veri türünü kontrol etme
var arrayVariable = [1, 2, 3];
console.log("Array Değişken Türü:", typeof arrayVariable);

// Function değişken tanımlama ve typeof ile veri türünü kontrol etme
var functionVariable = function() { console.log("Function çalıştı."); };
console.log("Function Değişken Türü:", typeof functionVariable);

// Undefined değişken tanımlama (ancak değer ataması yapmadan) ve typeof ile veri türünü kontrol etme
var undefinedVariable;
console.log("Undefined Değişken Türü:", typeof undefinedVariable);

// Null değişken tanımlama ve typeof ile veri türünü kontrol etme
var nullVariable = null;
console.log("Null Değişken Türü:", typeof nullVariable);