// Atanmamış dört değişken tanımlama
var uninitializedVar1;
var uninitializedVar2;
var uninitializedVar3;
var uninitializedVar4;

// Atanan değerlere sahip dört değişken tanımlama
var initializedVar1 = 10;
var initializedVar2 = "Hello";
var initializedVar3 = true;
var initializedVar4 = null;

// Konsola değişkenlerin değerlerini yazdırma
console.log("Atanmamış Değişken 1:", uninitializedVar1);
console.log("Atanmamış Değişken 2:", uninitializedVar2);
console.log("Atanmamış Değişken 3:", uninitializedVar3);
console.log("Atanmamış Değişken 4:", uninitializedVar4);

console.log("Atanan Değerli Değişken 1:", initializedVar1);
console.log("Atanan Değerli Değişken 2:", initializedVar2);
console.log("Atanan Değerli Değişken 3:", initializedVar3);
console.log("Atanan Değerli Değişken 4:", initializedVar4);