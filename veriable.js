// String değişken tanımlama ve atama
var stringVariable = "Bu bir string değişken";

// Boolean değişken tanımlama ve atama
var booleanVariable = true;

// Undefined değişken tanımlama (ancak değer ataması yapmadan)
var undefinedVariable;

// Null değişken tanımlama ve atama
var nullVariable = null;

// Değişkenleri konsola yazdırma
console.log("String Değişken:", stringVariable);
console.log("Boolean Değişken:", booleanVariable);
console.log("Undefined Değişken:", undefinedVariable);
console.log("Null Değişken:", nullVariable);